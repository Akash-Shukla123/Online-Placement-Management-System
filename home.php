<tr><td align="center"><h2 class="h">PLACEMENT PROCESS</h2><br />

<p>The Institute helps each student in exploring placement opportunities by inviting various companies for campus recruitment of students who are in the final year of the programme and are likely to graduate at the end of the academic year.</p>


<p>The final placements, at the Institute, are a result of very systematic interaction with the industry and continuous career counseling of the students. Right from the beginning of the programme, students are continuously counseled with regard to his/her career aspirations and options, which in turn is vigorously followed up with the potential companies for participating in the placement programme of the Institute. This not only helps the students in getting their ‘dream’ jobs but also assists the visiting placement companies in identifying the ‘right’ candidate for their organization. However, the placement will be governed by Placement guidelines:</p>

<br />
<h2 class="h">Planning for Placement</h2>
<br />

<p>The placement activity is primarily managed by the Placement Committee headed by Director of the institute as Chairperson with the help of the Manager Corporate Relations and other committee members which includes faculties and students from various departments.</p>
<br />

<h2 class="h">Student Registration for Placement</h2>

 

<p>Students who are in their final year and interested in availing placement support from institute, will have to register themselves for placement. Those who don’t want any kind of placement support need not register themselves. Registration for placement is done by paying placement registration fees decided by the institute from time to time.</p></td>
